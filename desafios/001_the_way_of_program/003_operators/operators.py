def add(first_number, second_number):
  # Retorne a soma dos parâmetros first_number e second_number
  return first_number + second_number

def sub(first_number, second_number):
  # Retorne a subtração dos parâmetros first_number e second_number
  return first_number - second_number

def mult(first_number, second_number):
  # Retorne a multiplicação dos parâmetros first_number e second_number
  return first_number * second_number

def div(first_number, second_number):
  # Retorne a divisão dos parâmetros first_number e second_number
  return first_number / second_number

def expo(first_number, second_number):
  # Retorne a exponenciação dos parâmetros first_number e second_number
  return first_number ** second_number

def remai(first_number, second_number):
  # Retorne o resto da divisão dos parâmetros first_number e second_number
  return first_number % second_number
